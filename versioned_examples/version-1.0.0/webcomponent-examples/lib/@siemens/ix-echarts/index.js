function registerEChartsThemes() {
    if (echarts__namespace === undefined) {
        console.error('echarts not found!');
    }
    [
        classicDarkProject,
        classicLightProject,
        brandDarkProject,
        brandLightProject,
    ].forEach((themeBundle) => {
        echarts__namespace.registerTheme(themeBundle.themeName, themeBundle.theme);
    });
}

